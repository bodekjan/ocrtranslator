    //创建jpeg图片回调数据对象
    private Camera.PictureCallback jpeg = new Camera.PictureCallback() {
        @Override
        public void onPictureTaken(byte[] data, Camera Camera) {
            BufferedOutputStream bos = null;
            Bitmap bm = null;
            String filePath="--";
            try {
                // 获得图片
                bm = BitmapFactory.decodeByteArray(data, 0, data.length);
                bm=toturn(bm);
                bm=imageCut(bm,true);
                if (Environment.getExternalStorageState().equals(Environment.MEDIA_MOUNTED)) {
                    Log.i(TAG, "Environment.getExternalStorageDirectory()="+Environment.getExternalStorageDirectory());
                    //filePath = path+System.currentTimeMillis()+".jpg";//照片保存路径
                    filePath = path+"screenshut.jpg";//照片保存路径
                    File fPath = new File(path);
                    File file = new File(filePath);
                    if (!file.exists()){
                        fPath.mkdirs();
                        file.createNewFile();
                    }
                    bos = new BufferedOutputStream(new FileOutputStream(file));
                    bm.compress(Bitmap.CompressFormat.JPEG, 100, bos);//将图片压缩到流中

                }else{
                    Toast.makeText(mContext,"没有检测到内存卡", Toast.LENGTH_SHORT).show();
                }
                /* 是否转之前弄好 */
                bm.recycle();// 回收bitmap空间
                Intent intent=new Intent(mContext,TranslateActivity.class);
                intent.putExtra("picpath",filePath);
                intent.putExtra("source",source);
                mContext.startActivity(intent);
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    bos.flush();//输出
                    bos.close();//关闭
                    bm.recycle();// 回收bitmap空间
                    mCamera.stopPreview();// 关闭预览
                    mCamera.startPreview();// 开启预览
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    };
    public static Bitmap imageCut(Bitmap bitmap, boolean isRecycled)
    {
        if (bitmap == null)
        {
            return null;
        }
        Bitmap bmp;
        try {
            int top=(int)(((float)bitmap.getHeight()/(float)CommonHelper.screenHeight)*CommonHelper.top);
            int left=(int)(((float)bitmap.getWidth()/(float)CommonHelper.screenWidth)*CommonHelper.left);
            int width=(int)(bitmap.getWidth()-Math.abs(((float)bitmap.getWidth()/(float)CommonHelper.screenWidth)*CommonHelper.right)-left);
            int height=(int)(bitmap.getHeight()-Math.abs(((float)bitmap.getHeight()/(float)CommonHelper.screenHeight)*CommonHelper.down)-top);
            bmp = Bitmap.createBitmap(bitmap, left, top, width,height , null, false);
//            bmp = Bitmap.createBitmap(bitmap, CommonHelper.left, CommonHelper.top, bitmap.getWidth()-Math.abs((bitmap.getWidth()/CommonHelper.screenWidth)*CommonHelper.right), bitmap.getHeight()-Math.abs((bitmap.getHeight()/CommonHelper.screenHeight)*CommonHelper.down), null,
//                    false);
        }catch (Exception e){
            bmp=bitmap;
        }

        if (isRecycled && bitmap != null && !bitmap.equals(bmp)
                && !bitmap.isRecycled())
        {
            bitmap.recycle();
            bitmap = null;
        }

        // 下面这句是关键
        return bmp;// Bitmap.createBitmap(bitmap, retX, retY, wh, wh, null,
        // false);
    }
    public void takePicture(String path, OneLanguage language){
        //设置参数,并拍照
        this.path=path;
        this.source=language.fileName;
        setCameraParams(mCamera, mScreenWidth, mScreenHeight);
        // 当调用camera.takePiture方法后，camera关闭了预览，这时需要调用startPreview()来重新开启预览
        mCamera.takePicture(null, null, jpeg);
    }
